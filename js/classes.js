class Team {
     constructor(name, abbrev, score){
          this.place = 0;
          this.name = name;
          this.abbrev = abbrev;
          this.score = score;
     }
}

class Series {
     constructor(date, team1, team2){
          this.date = date;
          this.team1 = team1;
          this.team2 = team2;
     }
}